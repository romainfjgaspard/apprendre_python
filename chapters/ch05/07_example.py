hp_values = [35, 78, 106, 45, 79]

print(f"Min HP: {min(hp_values)}")
print(f"Max HP: {max(hp_values)}")
print(f"Sum HP: {sum(hp_values)}")

# sorted() returns a new sorted list
print(f"Sorted: {sorted(hp_values)}")
print(f"Reversed: {sorted(hp_values, reverse=True)}")

# sort names alphabetically
names = ["Mewtwo", "Abra", "Pikachu", "Charizard"]
print(f"Sorted names: {sorted(names)}")