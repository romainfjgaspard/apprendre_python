print(type(35))          # <class 'int'>
print(type(3.14))        # <class 'float'>
print(type("Pikachu"))   # <class 'str'>
print(type(True))        # <class 'bool'>

# ⚠️ Watch out! "35" with quotes is text, not a number!
print(type("35"))        # <class 'str'>  ← text!
print(type(35))          # <class 'int'>  ← number!